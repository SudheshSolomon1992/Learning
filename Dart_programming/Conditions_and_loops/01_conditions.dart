void main(){
  var age = 30;

  // If condition
  if (age > 18){
    print ("You are a voter");
  }

  // If else condition
  if (age > 18){
    print ("You are a voter");
  } else {
    print ("You are not a voter");
  }

  // Condition based on boolean value
  bool isMarried = true;
  if (isMarried){
    print ("You are married");
  } else {
    print ("You are single");
  }

  // If-else-if condition
  int numMonth = 3;
  if (numMonth==1){
    print ("The month is January");
  } else if (numMonth==2){
    print ("The month is February");
  } else if (numMonth==3){
    print ("The month is March");
  } else if (numMonth==4){
    print ("The month is April");
  } else if (numMonth==5){
    print ("The month is May");
  } else if (numMonth==6){
    print ("The month is June");
  } else if (numMonth==7){
    print ("The month is July");
  } else if (numMonth==8){
    print ("The month is August");
  } else if (numMonth==9){
    print ("The month is September");
  } else if (numMonth==10){
    print ("The month is October");
  } else if (numMonth==11){
    print ("The month is November");
  } else if (numMonth==12){
    print ("The month is December");
  } else {
    print ("Invalid option given");
  }
}